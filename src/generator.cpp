Result<void> Generator::emit_rust(const Registry& reg, const std::string& path) {
    std::ofstream f(path);
    if (!f.is_open()) return std::unexpected(Diagnostic::build(ErrorCode::InternalError, "Could not open Rust file for writing: " + path));

    f << "// Generated by Zconfig\n\n";
    auto emit_r = [&](const std::string& name, bool visible, Type type, const Value& computed_value) {
        if (!visible) return;

        std::string rust_name = name;
        std::replace(rust_name.begin(), rust_name.end(), '.', '_');
        std::transform(rust_name.begin(), rust_name.end(), rust_name.begin(), ::toupper);

        if (type == Type::Bool) {
            f << "pub const CONFIG_" << rust_name << ": bool = " << (std::get<bool>(computed_value) ? "true" : "false") << ";\n";
        } else if (type == Type::Int) {
            f << "pub const CONFIG_" << rust_name << ": i64 = " << std::get<int64_t>(computed_value) << ";\n";
        } else if (type == Type::String || type == Type::Enum) {
            f << "pub const CONFIG_" << rust_name << ": &str = \"" << std::get<std::string>(computed_value) << "\";\n";
        } else if (type == Type::Set) {
            const auto& vec = std::get<std::vector<std::string>>(computed_value);
            f << "pub const CONFIG_" << rust_name << ": &[&str] = &[";
            for (size_t i = 0; i < vec.size(); ++i) {
                f << "\"" << vec[i] << "\"" << (i == vec.size() - 1 ? "" : ", ");
            }
            f << "];\n";
            for (const auto& item : vec) {
                std::string item_name = item;
                std::replace(item_name.begin(), item_name.end(), '.', '_');
                std::transform(item_name.begin(), item_name.end(), item_name.begin(), ::toupper);
                f << "pub const CONFIG_" << rust_name << "_" << item_name << ": bool = true;\n";
            }
        }
    };

    for (const auto& [name, sym] : reg.get_symbols()) {
        emit_r(name, sym->is_effectively_visible(), sym->type, sym->computed_value);
    }
    for (const auto& [name, sym] : reg.get_computed()) {
        emit_r(name, sym->is_effectively_visible(), sym->type, sym->value);
    }
    return {};
}

